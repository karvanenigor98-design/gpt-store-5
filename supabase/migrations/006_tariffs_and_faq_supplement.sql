-- Проверить: есть ли таблица tariffs
-- SELECT to_regclass('public.tariffs');

-- Если таблица tariffs существует:
alter table public.tariffs
  add column if not exists requires_additional_data boolean not null default false,
  add column if not exists activation_note text,
  add column if not exists data_requirement_text text,
  add column if not exists site_id uuid references public.sites(id) on delete set null;

create index if not exists tariffs_site_id_idx on public.tariffs(site_id);

-- ============================================================
-- 2. Таблица faq_items — если хранятся в БД
-- ============================================================

-- Проверить: есть ли таблица faq_items
-- SELECT to_regclass('public.faq_items');

-- Если таблица faq_items существует:
alter table public.faq_items
  add column if not exists site_id uuid references public.sites(id) on delete cascade;

create index if not exists faq_items_site_id_idx on public.faq_items(site_id);

-- ============================================================
-- 3. Добавляем site_id в promocodes (если ещё нет)
-- ============================================================

alter table public.promocodes
  add column if not exists site_id uuid references public.sites(id) on delete set null;

create index if not exists promocodes_site_id_idx on public.promocodes(site_id);

-- ============================================================
-- 4. Добавляем site_id в landing_discounts / discounts
-- ============================================================

-- landing_discounts (проверить имя таблицы)
